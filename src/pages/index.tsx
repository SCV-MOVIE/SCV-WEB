import Head from 'next/head';
import styled from '@emotion/styled';
import { Grid } from '@chakra-ui/react';

import { DUMMY_MOVIES } from '@/constants/dummy';
import { Bottom, LinedTitle, MovieCard, NavBar } from '@/components';
import { GetServerSideProps } from 'next';
import { Movie } from '../@types';
import { api } from '../api';

interface Props {
  movies: Movie[];
}

export default function MainPage({ movies }: Props) {
  return (
    <>
      <Head>
        <title>Create Next Apps</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <NavBar />

      <Content>
        <LinedTitle title={'현재 상영작'} />
        <Grid templateColumns="repeat(4, 1fr)" rowGap={24} columnGap={8} mt={8}>
          {movies.map((movie) => (
            <MovieCard movie={movie} key={movie.id} />
          ))}
        </Grid>
      </Content>
      <Bottom />
    </>
  );
}

const Content = styled.div`
  display: block;
  padding-inline: 40px;

  ${({ theme }) =>
    `@media (max-width: ${theme.bp.mobile}) {
      padding-inline: 24px;
    }`}
`;

export const getServerSideProps: GetServerSideProps<Props> = async (context) => {
  try {
    const result = await api.get<Movie[]>('/api/movie/list');

    return {
      props: {
        movies: result.data,
      },
    };
  } catch (error) {
    return {
      props: {
        movies: [],
      },
    };
  }
};
